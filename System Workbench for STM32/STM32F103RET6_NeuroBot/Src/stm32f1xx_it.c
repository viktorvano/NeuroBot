/**
* @brief This function handles TIM2 global interrupt.
*/
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	  if(!HAL_GPIO_ReadPin(BUTTON_OK_GPIO_Port, BUTTON_OK_Pin))
	  {
		  OK.pressed=1;
		  OK.released=0;
	  }
	  else if((OK.pressed==1)&&(HAL_GPIO_ReadPin(BUTTON_OK_GPIO_Port, BUTTON_OK_Pin)))
	  {
		  OK.pressed=0;
		  OK.released=1;
		  motor_stop();
	  }

	  if(!HAL_GPIO_ReadPin(BUTTON_UP_GPIO_Port, BUTTON_UP_Pin))
	  {
		  UP.pressed=1;
		  UP.released=0;
	  }
	  else if((UP.pressed==1)&&(HAL_GPIO_ReadPin(BUTTON_UP_GPIO_Port, BUTTON_UP_Pin)))
	  {
		  UP.pressed=0;
		  UP.released=1;
		  motor_stop();
	  }

	  if(!HAL_GPIO_ReadPin(BUTTON_DOWN_GPIO_Port, BUTTON_DOWN_Pin))
	  {
		  DOWN.pressed=1;
		  DOWN.released=0;
	  }
	  else if((DOWN.pressed==1)&&(HAL_GPIO_ReadPin(BUTTON_DOWN_GPIO_Port, BUTTON_DOWN_Pin)))
	  {
		  DOWN.pressed=0;
		  DOWN.released=1;
		  motor_stop();
	  }

	  if(HAL_GPIO_ReadPin(BUTTON_POWER))
	  {
		  POWER.pressed=1;
		  POWER.released=0;
	  }
	  else if((POWER.pressed==1)&&(!HAL_GPIO_ReadPin(BUTTON_POWER)))
	  {
		  POWER.pressed=0;
		  POWER.released=1;
		  motor_stop();
	  }

	  Battery=0.995f*Battery + 0.005f*(float)ADC[0];

		percent=((float)Battery*1.89512-3500.0f)/6.350f;
		if(percent>100)
			percent=100;
		if(percent<=0)
			NeuroBot_sleep();

		if(cnt_flag==1 && cnt_20ms<250)
			cnt_20ms++;
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

